
public class Swap {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int temp;
		System.out.println("Before swaping a is:"+a);
		System.out.println("Before swaping b is:"+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("After swaping of a:"+a);
		System.out.println("After swaping of b:"+b);
		
				

	}

}
